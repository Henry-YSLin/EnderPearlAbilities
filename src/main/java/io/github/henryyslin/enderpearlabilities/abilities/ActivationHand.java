package io.github.henryyslin.enderpearlabilities.abilities;

/**
 * The activation hand of an ability. Players need to hold ender pearls in
 * the specified activation hand to activate the ability. Each player can have
 * at most 1 ability in their main hand and 1 in their off hand.
 */
public enum ActivationHand {
    MainHand,
    OffHand,
}
